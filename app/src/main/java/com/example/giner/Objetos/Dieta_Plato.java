
public class Dieta_Plato {
	
	private int id_dieta;
	private int dia_semana;
	private int tipo_comida;
	private int plato;
	
	
	public int getId_dieta() {
		return id_dieta;
	}
	public void setId_dieta(int id_dieta) {
		this.id_dieta = id_dieta;
	}
	public int getDia_semana() {
		return dia_semana;
	}
	public void setDia_semana(int dia_semana) {
		this.dia_semana = dia_semana;
	}
	public int getTipo_comida() {
		return tipo_comida;
	}
	public void setTipo_comida(int tipo_comida) {
		this.tipo_comida = tipo_comida;
	}
	public int getPlato() {
		return plato;
	}
	public void setPlato(int plato) {
		this.plato = plato;
	}
	
	

}
